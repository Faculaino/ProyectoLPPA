using Lppa.Entities;
using Lppa.UI.Process;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Mvc;

namespace Lppa.UI.Web.Controllers
{
    public class UsuarioController : Controller
    {
        // GET: Usuario
        public ActionResult Index()
        {
            var uc = new UsuarioComponentController();
            return View(uc.SeleccionarUsuarios());
        }

        public ActionResult Create()
        {
            return View();
        }
        [HttpPost]
        public ActionResult Create(Usuario user)
        {
            var uc = new UsuarioComponentController();
            uc.AgregarUnUsuario(user);
            return RedirectToAction("Index");
        }

        public ActionResult Edit()
        {
            return View();
        }
        [HttpPost]
        public ActionResult Edit(Usuario user)
        {
            var uc = new UsuarioComponentController();
            uc.AgregarUnUsuario(user);
            return RedirectToAction("Index");
        }
    }
}